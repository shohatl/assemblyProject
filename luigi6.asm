;input - bp+4 location of the upper left pixel color, bp+6 the location of the upper left pixel on the screen
;bp+8 - number of collumns, bp+10 number of rows
;output - es: 0 - 63999 
;prints a frame on the screen
proc print
push bp
mov bp,sp
push bx
push cx
push ax
push di
push si

    mov si,[bp+4]
    mov bx,[bp+10]
	mov cx,bx	;number of rows

	mov bx,[bp+6]
	mov di,[bx]	;upper left pixel
	mov ax,di
    mov bx,[bp+8]   ;number of collumns 
	add ax,bx	;number of rows

	sameline:
        push ax
            mov al,[si]
            cmp al,36	;if pink skip
            je skip
                mov [es:di],al
            skip:
            inc si
            inc di
        pop ax
        cmp ax,di
	jne sameline

	sub di,bx	;next line
	add di,320	
	mov ax,di	;next stopper
	add ax,bx	
	loop sameline
		
pop si
pop di
pop ax
pop cx
pop bx
pop bp
ret 8
endp print
