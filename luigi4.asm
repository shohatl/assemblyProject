	mov di,[bx]	;upper left pixel
	mov ax,di
    mov bx,[bp+8]   ;number of collumns 
	add ax,bx	;number of rows

	sameline:
        push ax
            mov al,[si]
            cmp al,36	;if pink skip
            je skip
                mov [es:di],al
            skip:
            inc si
            inc di
        pop ax
        cmp ax,di
	jne sameline

