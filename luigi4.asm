;buffer+0 = q, buffer+1 = a, buffer+2 = d, buffer+3 = w, buffer+4 = left arrow
proc input
push    ax
push    bx
push    cx
push    dx
push    di
push    si
	
in al,60h	;gets the scan code from the keyboard

xor bx,bx ;resets bl becuase it will check the buttons
mov bl,al
and bl, 01111111b	;points only to the key

cmp bl,10h ;'q' 
jne not_q
	mov bl,0	;'q' is in buffer+0
	jmp found_it
not_q:

cmp bl,1eh ;'a'
jne not_a
	mov bl,1	;'a' is in buffer+1
	jmp found_it
not_a:

cmp bl,20h ;'d' 
jne not_d
	mov bl,2	;'d' is in buffer+2
	jmp found_it
not_d:

cmp bl,11h ;'w' 
jne not_w
	mov bl,3	;'w' is in buffer+3
	jmp found_it
not_w:

cmp bl,4bh ;left arrow 
jne not_left
	mov bl,4	;left arrow is in buffer+4
	jmp found_it
not_left:

found_it:
shr al,7	;gets the info (pressed - 0, released - 1)
xor al,1	;makes 1 - pressed and 0 - released
mov [cs:(buffer+bx)], al

mov bx,offset buffer	;checks if there is something in the buffer
mov cx,11
check_input:
	mov al,[cs:bx]
	cmp al,1
	je end_check
	inc bx
loop check_input

	mov bx,offset new_input	;if there isn't put zero in new_input
	mov [byte ptr cs:bx],0
	jmp no_input

end_check:	;if there is put one in new_input
	mov bx,offset new_input	
	mov [byte ptr cs:bx],1

no_input:

in      al,61H             ;get value of keyboard control lines
mov     ah,al              ; save it
or      al,80h             ;set the "enable kbd" bit
out     61H,al             ; and write it out the control port
xchg    ah,al              ;fetch the original control port value
out     61H,al             ; and write it back
mov     al,20H             ;send End-Of-Interrupt signal
out     20H,al             ; to the 8259 Interrupt Controller

pop     si
pop     di                       ;
pop     dx
pop     cx
pop     bx
pop     ax
iret
endp input
